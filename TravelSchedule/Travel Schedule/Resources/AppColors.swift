import SwiftUI

enum AppColors {
    enum Universal {
        static let black = Color.ccAlwaysBlack
        static let white = Color.ccAlwaysWhite
        static let gray = Color.ccGray
        static let lightGray = Color.ccLightGray
        static let darkGray = Color.ccGray
        static let blue = Color.ccBlue
        static let red = Color.ccRed
    }
    
    enum LightDark {
        static let black = Color.ccBlack
        static let white = Color.ccWhite
    }
}
